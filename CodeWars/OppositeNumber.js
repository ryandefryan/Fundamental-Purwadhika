// Ketentuan : Mengubah Angka Positif Menjadi Negatif (Begitu Juga Sebaliknya)

function opposite(number) {
    
    var oppositeResult = 0
    oppositeResult = oppositeResult - number

    return oppositeResult
}

console.log(opposite(10))
console.log(opposite(0))
console.log(opposite(-15))